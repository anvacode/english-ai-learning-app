/// Extension para facilitar el uso de iconos
extension IconExtension on IconData {
  /// Crea un icon widget rápidamente
  Widget toIcon({Color? color, double size = 24}) {
    return Icon(this, color: color, size: size);
  }

  /// Crea un icono con fondo circular
  Widget toCircularIcon({
    required Color backgroundColor,
    Color iconColor = Colors.white,
    double size = 24,
    double padding = 12,
  }) {
    return AppIcons.getCircularIcon(
      icon: this,
      backgroundColor: backgroundColor,
      iconColor: iconColor,
      size: size,
      padding: padding,
    );
  }
}
