  /// Obtiene un icono con estilo circular de fondo
  static Widget getCircularIcon({
    required IconData icon,
    required Color backgroundColor,
    Color iconColor = Colors.white,
    double size = 24,
    double padding = 12,
  }) {
    return Container(
      padding: EdgeInsets.all(padding),
      decoration: BoxDecoration(
        color: backgroundColor,
        shape: BoxShape.circle,
        boxShadow: [
          BoxShadow(
            color: backgroundColor.withAlpha(40),
            blurRadius: 8,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Icon(icon, color: iconColor, size: size),
    );
  }

  /// Obtiene un icono con estilo de tarjeta
  static Widget getCardIcon({
    required IconData icon,
    required Color backgroundColor,
    Color iconColor = Colors.white,
    double size = 32,
    double borderRadius = 16,
  }) {
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: backgroundColor,
        borderRadius: BorderRadius.circular(borderRadius),
        boxShadow: [
          BoxShadow(
            color: backgroundColor.withAlpha(30),
            blurRadius: 12,
            offset: const Offset(0, 6),
          ),
        ],
      ),
      child: Icon(icon, color: iconColor, size: size),
    );
  }

  /// Obtiene un icono animado simple
  static Widget getAnimatedIcon({
    required IconData icon,
    required Color color,
    double size = 24,
  }) {
    return TweenAnimationBuilder<double>(
      tween: Tween(begin: 0.8, end: 1.0),
      duration: const Duration(milliseconds: 300),
      builder: (context, value, child) {
        return Transform.scale(
          scale: value,
          child: Icon(icon, color: color, size: size),
        );
      },
    );
  }
